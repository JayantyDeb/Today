package interm;

public class SwitchCaseDemo {

	public static void main(String[] args) {
		int day;
		day = 6;
		switch (day) {
		case 1:
			System.out.println("Monday");
			break;
		case 2:
			System.out.println("Tuesday");
		case 3:
			System.out.println("Wednesday");
		default:
			System.out.println("Invalid Input");
		}

	}

}
